	void GenerateGrid()
	{
		float lift = 0f;
		int index_wall = 0;
		int index_base = 0;
		int chance = 0; //chance to spawn
		GameObject obj;

		for (int i = 0; i < columns; i++)
		{
			for (int j = 0; j < rows; j++)
			{
				if (gridProps[i.ToString() + "__" + j.ToString()] == "wall") {
					lift = 1f + Random.Range(-5, 0) / 100f;
					index_wall = Random.Range(0, 4);
					if (index_wall == 0) {lift = 1f + Random.Range(-15, 15) / 100f;}
					obj = Instantiate(walls[index_wall], Vector3.zero, walls[index_wall].transform.rotation);
				}
				else {

					chance = Random.Range(0, 4);
					if (chance == 1) {

						if (gridProps[i.ToString() + "__" + j.ToString()] != "wall") {
							lift = 0.33f;
							obj = Instantiate(apple, Vector3.zero, apple.transform.rotation);
							obj.transform.parent = transform;
							obj.transform.localPosition = new Vector3(LeftBottomLocation.x + scale * i, LeftBottomLocation.y + lift, LeftBottomLocation.z + scale * j);
							obj.name = i.ToString() + "_apple_" + j.ToString();
							FRUITs += 1;
						}

					}


					chance = Random.Range(0, 12);
					if (chance == 1) {

						if (gridProps[i.ToString() + "__" + j.ToString()] != "wall") {
							lift = 0.43f + Random.Range(-5, 5) / 100f;
							obj = Instantiate(stone, Vector3.zero, stone.transform.rotation);
							obj.transform.parent = transform;
							obj.transform.localPosition = new Vector3(LeftBottomLocation.x + scale * i, LeftBottomLocation.y + lift, LeftBottomLocation.z + scale * j);
							chance = Random.Range(0, 4);
							obj.transform.Rotate(RotateAmount * chance);
							obj.name = i.ToString() + "_stone_" + j.ToString();

						}

					}

					lift =  0f;//Random.Range(-3, 3)/100f;
					index_base = Random.Range(0, 5);
					obj = Instantiate(bases[index_base], Vector3.zero, bases[index_base].transform.rotation);
				}



				obj.transform.parent = transform;
				obj.transform.localPosition = new Vector3(LeftBottomLocation.x + scale * i, LeftBottomLocation.y + lift, LeftBottomLocation.z + scale * j);
				chance = Random.Range(0, 4);
